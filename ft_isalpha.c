#include <stdio.h>

int	ft_isalpha(int str)
{
	int	alpha;

	alpha = 0;
	if ((str >= 'A' && str <= 'Z' ) || (str >= 'a' && str <= 'z' ))
	{
		alpha = 1;
	}
	return (alpha);
}
